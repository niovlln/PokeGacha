// Lookup with {placeholder} interpolation. Falls back to English, then to the key itself.
function t(key, vars) {
  const lang = (typeof G !== 'undefined' && G.lang) ? G.lang : 'en';
  let str = (STRINGS[lang] && STRINGS[lang][key]) || STRINGS.en[key] || key;
  if (vars) for (const k in vars) str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
  return str;
}

// Translated type label (display only — `type` id stays English in all logic).
function typeLabel(type) { return t('t_' + type); }

// Walk the DOM and fill every [data-i18n] / [data-i18n-ph] element.
function applyStaticI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
  });
}

// Switch language: persist + re-render everything currently visible.
function setLang(code) {
  G.lang = code;
  save();
  applyStaticI18n();
  if (typeof renderShop === 'function') renderShop();
  if (typeof renderCollection === 'function') renderCollection();
  if (typeof renderSettings === 'function') renderSettings();
  if (typeof updateHUD === 'function') updateHUD();
  if (typeof updatePityBar === 'function') updatePityBar();
}